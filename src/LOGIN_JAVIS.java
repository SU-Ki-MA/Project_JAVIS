    private void jPFpassKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPFpassKeyPressed
    if(jPFpass.getForeground()!=new Color(200,200,200)){
        if(jPFpass.getText().equals("Password")){
            jPFpass.setText("");
            if(jTBTNvis.isSelected()){
               jPFpass.setEchoChar((char)0);
            }
            else if(jTBTNvis.isSelected()==false){
               jPFpass.setEchoChar('*');
            }
        }
    }
    jPFpass.setForeground(new Color(200,200,200));
    }//GEN-LAST:event_jPFpassKeyPressed

