    private void jBTNsigninMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBTNsigninMouseClicked
    boolean u=false,p=false;
    if(jTFuser.getText().equals("User Name")){
        JOptionPane.showMessageDialog(null, "Enter a valid User Name \n 'User Name' will not be accepted as an entry", "Invalid User Name", 0,null);
    }else if(jTFuser.getText().equals("")){
        JOptionPane.showMessageDialog(null, "User name is empty!!!", "Invalid User Name", 0,null);
    }else{
        u=true;
    }
    if(jPFpass.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Password is empty!!!", "Invalid password", 0,null);
    }else{
        p=true;
    }
    if(u==true){
        if(p==true){
             try {
                 Class.forName("com.mysql.jdbc.Driver").newInstance();
                 String db_url="jdbc:mysql://localhost:3306/project_JAVIS";
                 Connection conn= DriverManager.getConnection(db_url, "root", "mysql");
                 Statement smt=conn.createStatement();
                 //Getting input data 
                 String username=jTFuser.getText();
                 String Password=jPFpass.getText();
                 
                 //Getting data from the server
                 String sql="select * from clients_manage_string_JAVIS where USER_ID='"+username+"';";
                 ResultSet rs=smt.executeQuery(sql);
                 
                 //Decryption and Encryption of password
                 String encrypted_password="";
                 String brand="";
                 String mrand="";
                 String frand="";
                 String userID="";
                 while(rs.next()){
                 userID=rs.getString("USER_ID");
                 encrypted_password=rs.getString("pass_string");
                 brand=rs.getString("b_string");
                 mrand=rs.getString("m_string");
                 frand=rs.getString("f_string");
                 }
                 int mid=Password.length();
                 String midtext1=Password.substring(0,mid/2);
                 String midtext2=Password.substring(mid/2);
                 String decrypted=brand+midtext1+mrand+midtext2+frand;
                 
                 //Verifing password
                 //Verifing user
                 
                 if(userID.equals("")){
                     JOptionPane.showMessageDialog(null, "User Name not Available", "Unknown user", 0,null);
                 }else{
                 if(encrypted_password.equals(decrypted)){
                     JOptionPane.showMessageDialog(null, "Successfully Logged in"+"\n Welcome Back "+username, "Login successful", 0,new ImageIcon(getClass().getClassLoader().getResource("Icons\\icons8_Good_Quality_48px.png")));
                     JAVIS_OFFICIAL_SITE os=new JAVIS_OFFICIAL_SITE();
                     os.jLBLusername_os.setText(""+userID);
                     os.setVisible(true);
                     os.setLocationRelativeTo(null);
                     os.setSize(1282,629);
                     this.setVisible(false);
                     
                     //updating data
                     try{
                        Class.forName("com.mysql.jdbc.Driver").newInstance();
                        String sql1="update clients_javis set LAST_SIGN_IN=now() where user_id='"+userID+"';";
                        int rc=smt.executeUpdate(sql1);
                        smt.close();
                        conn.close();
   
                     }
                    catch(Exception e){
                        JOptionPane.showMessageDialog(null,"Error in Data update :\n"+e);
                    }
                 }else{
                     JOptionPane.showMessageDialog(null, "Sorry the password did not match", "Incorrect Password", 0,null);
                 }
                 }
                 rs.close();
                 smt.close();
                 conn.close();

                 }
                catch(Exception e){
                 JOptionPane.showMessageDialog(null,"Error in connection to the server :\n"+e);
                }
        }
    }
    }//GEN-LAST:event_jBTNsigninMouseClicked
